    private void calcular() {
        importepagado = Float.parseFloat(this.entregado.getText());
        saldo = Float.parseFloat(cobrar.Saldo.getText());
        importeCobrar = Float.parseFloat(cobrar.montoCobrar.getText());
        operacion = (importepagado - importeCobrar);

        
        if (operacion > 0) {
            cobros.txtpagado.setText(this.entregado.getText());
            cobrar.Saldo.setText("0");
            cobrar.montoCobrar.setText("0");
            this.entregado.setText("0");
            this.entregado.setEnabled(false);
            this.cambio.setText(String.valueOf(operacion));
            cobros.txtcambio122233.setText(this.cambio.getText().replace(".0", ""));
            cobros.txtcambio1.setText(this.cambio.getText().replace(".0", ""));
            cobros.txtsaldo.setText(cobrar.Saldo.getText().replace(".0", ""));
            
        }  if (operacion < 0) {
            cobrar.Saldo.setText(String.valueOf(operacion * -1));
            cobrar.montoCobrar.setText(String.valueOf(operacion * -1));
            this.cambio.setText("0");
            this.entregado.setText("0");
            entregado.requestFocus();
            this.entregado.setText("0");
            
        }  if (operacion == 0) {
             this.cambio.setText("0");
            this.entregado.setText("0");
            cobrar.montoCobrar.setText("0");
            cobrar.Saldo.setText("0");
            cobros.txtcambio122233.setText(this.cambio.getText().replace(".0", ""));
            cobros.txtcambio1.setText(this.cambio.getText().replace(".0", ""));
            cobros.txtsaldo.setText(cobrar.Saldo.getText().replace(".0", ""));

        }
    }

    private void dehabilitarBilletes() {
        saldo = Integer.parseInt(cobrar.Saldo.getText());
        if (saldo == 0) {
            this.B10000.setEnabled(false);
            this.B20000.setEnabled(false);
            this.B50000.setEnabled(false);
            this.B100000.setEnabled(false);
            dispose();

        }
    }

    private void validadPago() {
        if (Float.parseFloat(Saldo.getText()) < 1) {
            cobros.BGravar1.setEnabled(true);
            dispose();
        }
    }
    
    
