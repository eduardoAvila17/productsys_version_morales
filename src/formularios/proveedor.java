         private void cargar_grilla()
  {
      try
      {
           for (int i = grilla.getRowCount()-1; i>=0; i--)
            {
                cursor.removeRow(i);
            }
 
          
          
            ver_conex conn = new ver_conex();
            conn.sentencia = conn.conexion.createStatement();
            conn.resultado = conn.sentencia.executeQuery("SELECT * from proveedor as p inner join ciudad as c on p.ciu_cod=c.ciu_cod");
           
            while(conn.resultado.next())
            {
                Object[] datos = {conn.resultado.getString("provee_cod"),
                    conn.resultado.getString("ciu_cod"),
                    conn.resultado.getString("ciu_desc"),
                    conn.resultado.getString("provee_nom"),
                    conn.resultado.getString("provee_dir"),
                    conn.resultado.getString("provee_tel"),
                    conn.resultado.getString("provee_ruc"),
                    conn.resultado.getString("prove_mail"),
                    conn.resultado.getString("prove_est"),
                };
                cursor.addRow(datos);   
            }

        } catch (SQLException ex) {
            Logger.getLogger(proveedor.class.getName()).log(Level.SEVERE, null, ex);
        }
  }
