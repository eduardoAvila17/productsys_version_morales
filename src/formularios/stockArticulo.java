    private void buscarcod() throws SQLException {
        ver_conex conn = new ver_conex();
        conn.sentencia = conn.conexion.createStatement();
        conn.resultado = conn.sentencia.executeQuery("SELECT prd_cod,barra,descripcion,costo,precio_venta\n"
                + "FROM producto\n"
                + "WHERE estado = 1\n"
                + "AND barra =" + this.producod.getText());
        if (conn.resultado.next())//si encontro
        {
            this.produdesc.setText(conn.resultado.getString("descripcion"));
            this.actual.setText(conn.resultado.getString("precio_venta"));
            this.costo.setText(conn.resultado.getString("costo"));
            //this.txtcantidad.setText(conn.resultado.getString("p"));
            // iva = conn.resultado.getString("imp_cod");

            this.producod.setEnabled(false);
            this.nuevoPrecio.setEnabled(true);
            this.nuevoPrecio.requestFocus();
        } else//no encontro
        {
            JOptionPane.showMessageDialog(null, "CODIGO DE PRODUCTO NO EXISTE O SE ENCUENTRA INACTIVO", "MENSAJE",
                    JOptionPane.INFORMATION_MESSAGE);
             this.producod.setEnabled(false);
              this.nuevoPrecio.setEnabled(false);
              this.producod.requestFocus();
            
        }

    }

