    private void grabar() throws SQLException {
        ver_conex conn = new ver_conex();
        conn.sentencia = conn.conexion.createStatement();

        if (bandera.equals("agregar")) {
            gencod();// sirve para que no se dupliquen los codigos y no de error cuando se trabaja en redes///////////////////////////////////////////////////prd_cod,////////////////tip_prd_cod,/////////presen_cod,/////////////mar_cod,/////////////unid_cod,/////////////prd_nom,//////////////////prd_costo,/////////////iva,////matp_est,//////   grup_cod
            conn.sentencia.executeUpdate("INSERT INTO cabecera_precios (id,fecha,hora,usuario,estado) VALUES (" + idCambio.getText() + ",'" + fecha.getText() + "','" + hora.getText() + "','" + usr.getText() + "'," + 1 + ")");
            //graba en el detalle

            for (int fila = 0; fila < grillacambioprecio.getRowCount(); fila++) {
                conn.sentencia = conn.conexion.createStatement();
                conn.sentencia.executeUpdate("INSERT INTO detalle_precios(id_cabecera,plu,descripcion,costo,prec_actual,prec_nuevo,estado)values(" + idCambio.getText() + "," + grillacambioprecio.getValueAt(fila, 1) + ",'" + grillacambioprecio.getValueAt(fila, 2) + "'," + grillacambioprecio.getValueAt(fila, 3) + "," + grillacambioprecio.getValueAt(fila, 4) + "," + grillacambioprecio.getValueAt(fila, 5) + "," + 1 + ")");
                conn.sentencia = conn.conexion.createStatement();
                conn.sentencia.executeUpdate("UPDATE producto SET precio_venta = " + grillacambioprecio.getValueAt(fila, 5) + " WHERE barra = " + grillacambioprecio.getValueAt(fila, 1));
            }
            JOptionPane.showMessageDialog(null, "REGISTRO GRABADO", "CONFIRMACION", JOptionPane.INFORMATION_MESSAGE);
limpiar();
        }

    }

